def test_resolver_includes_equipped_items_in_identity_version() -> None:
    plain = resolve_character_identity(_ian_profile(), "roblox_obby")
    backpack = resolve_character_identity(
        _ian_profile({"backpack": "brown_backpack"}),
        "roblox_obby",
    )

    assert backpack.equipped_items == {"backpack": "brown_backpack"}
    assert backpack.identity_version != plain.identity_version


