def test_character_judge_accepts_boolean_approval_values() -> None:
    from companion import character_judge

    approved = character_judge._coerce_result(
        {"approved": True, "score": 9, "issues": []},
        "test",
    )
    rejected = character_judge._coerce_result(
        {"approved": False, "score": 3, "issues": ["identity changed"]},
        "test",
    )
    passed_alias = character_judge._coerce_result(
        {"passed": True, "score": 8, "issues": []},
        "test",
    )

    assert approved.approved is True
    assert rejected.approved is False
    assert passed_alias.approved is True


