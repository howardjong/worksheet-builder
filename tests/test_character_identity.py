def test_variant_judging_delegates_to_shared_character_judge(monkeypatch: MonkeyPatch) -> None:
    from companion import generate_overlays

    observed: dict[str, object] = {}

    def fake_judge(
        reference_bytes: bytes,
        generated_bytes: bytes,
        criteria: list[str],
    ) -> CharacterJudgeResult:
        observed["reference"] = reference_bytes
        observed["generated"] = generated_bytes
        observed["criteria"] = criteria
        return CharacterJudgeResult(
            available=True,
            approved=True,
            score=8,
            issues=["minor edge cleanup"],
            judge="fake",
        )

    monkeypatch.setattr(generate_overlays, "judge_character_consistency", fake_judge)

    verdict = generate_overlays._judge_variant(
        b"reference bytes",
        b"generated bytes",
        {"backpack": "brown_backpack"},
    )

    assert verdict == {
        "passed": True,
        "score": 8,
        "issues": ["minor edge cleanup"],
    }
    assert observed["reference"] == b"reference bytes"
    assert observed["generated"] == b"generated bytes"
    criteria = observed["criteria"]
    assert isinstance(criteria, list)
    assert any("same character" in criterion.lower() for criterion in criteria)


