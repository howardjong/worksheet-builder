from companion.schema import AvatarConfig, CharacterStyleSheet, LearnerProfile


def _ian_profile(equipped_items: dict[str, str] | None = None) -> LearnerProfile:
    return LearnerProfile(
        name="Ian",
        grade_level="1",
        avatar=AvatarConfig(
            base_character="ian_learning_buddy",
            equipped_items=equipped_items or {},
            style_sheet=CharacterStyleSheet(
                character_block="Ian has rainbow spiky hair and a blue lightning shirt.",
                reference_image_dir="assets/style_sheets/ian_roblox_buddy",
                scene_guidelines="Use calm printable learning panels.",
                item_style_notes="Accessories must not hide Ian's hair or lightning shirt.",
                theme_id="roblox_obby",
            ),
        ),
    )


