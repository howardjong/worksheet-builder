def test_selected_reference_content_changes_identity_version(tmp_path: Path) -> None:
    ref_dir = tmp_path / "refs"
    ref_dir.mkdir()
    front_ref = ref_dir / "ref_front_character_crop.png"
    front_ref.write_bytes(b"first reference bytes")
    profile = LearnerProfile(
        name="Ian",
        grade_level="1",
        avatar=AvatarConfig(
            base_character="ian_learning_buddy",
            style_sheet=CharacterStyleSheet(
                character_block="Ian has rainbow spiky hair.",
                reference_image_dir=str(ref_dir),
            ),
        ),
    )

    first = resolve_character_identity(profile, "roblox_obby")
    front_ref.write_bytes(b"changed reference bytes")
    second = resolve_character_identity(profile, "roblox_obby")

    assert second.identity_version != first.identity_version


def test_selected_reference_touch_keeps_identity_version_stable(tmp_path: Path) -> None:
    ref_dir = tmp_path / "refs"
    ref_dir.mkdir()
    front_ref = ref_dir / "ref_front_character_crop.png"
    front_ref.write_bytes(b"same reference bytes")
    profile = LearnerProfile(
        name="Ian",
        grade_level="1",
        avatar=AvatarConfig(
            base_character="ian_learning_buddy",
            style_sheet=CharacterStyleSheet(
                character_block="Ian has rainbow spiky hair.",
                reference_image_dir=str(ref_dir),
            ),
        ),
    )

    first = resolve_character_identity(profile, "roblox_obby")
    stat = front_ref.stat()
    os.utime(
        front_ref,
        ns=(stat.st_atime_ns + 1_000_000_000, stat.st_mtime_ns + 1_000_000_000),
    )
    second = resolve_character_identity(profile, "roblox_obby")

    assert second.identity_version == first.identity_version
